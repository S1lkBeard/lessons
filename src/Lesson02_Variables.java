public class Lesson02_Variables {
    public static void main(String[] args) {
        int myInt;
        myInt = 5;
        System.out.println(myInt);

        int myIntSecond = 55;
        System.out.println(myIntSecond);

        //для 8-битных чисел
        byte myByte = 127;

        //для 16-битных чисел
        short myShort = 11111;

        //для 64-битных чисел
        long myLong = 43294234;

        //для 32-битных дробных чисел
        float myFloat = 432.43432F;

        //для 64-битных дробных чисел
        double myDouble = 432.431123;

        //для символов
        char myChar = 'a';

        //для логичческих операций
        boolean myBoolean = true;
    }
}
